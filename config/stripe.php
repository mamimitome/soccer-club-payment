<?php

/**
 * Stripe決済設定ファイル
 *
 * 設定ファイルとは？
 * アプリケーション全体で使う設定値を一箇所にまとめたファイルです。
 * config('stripe.secret') のように呼び出して使います。
 *
 * .env ファイルとの関係：
 * APIキーなどの機密情報は .env ファイルに書き、
 * ここでは env() 関数で読み込みます。
 * .env はGitに含めない（.gitignoreに記載）ため、
 * キーが外部に漏れるリスクを減らせます。
 *
 * Stripeのキーの種類：
 * - 公開可能キー (pk_...) : フロントエンド（ブラウザ）で使う。漏れても比較的安全
 * - シークレットキー (sk_...) : バックエンドのみで使う。絶対に漏らしてはいけない
 * - Webhookシークレット (whsec_...) : Webhookの署名検証に使う
 *
 * テスト環境と本番環境：
 * - テスト用キー: pk_test_..., sk_test_... で始まる
 * - 本番用キー:   pk_live_..., sk_live_... で始まる
 */

return [

    /**
     * Stripeの公開可能キー（フロントエンド用）
     *
     * Stripe.js（ブラウザ上のJavaScript）がカード情報を
     * Stripeのサーバーに直接送るために使います。
     * このキーはHTMLに埋め込まれるため、ユーザーから見えます。
     * しかしこのキーだけでは決済を実行できないため、漏れても安全です。
     *
     * 取得場所: Stripeダッシュボード → 開発者 → APIキー → 公開可能キー
     */
    'key' => env('STRIPE_KEY', ''),

    /**
     * Stripeのシークレットキー（バックエンド専用）
     *
     * サーバー側でPaymentIntentを作成したり、
     * 決済を実行したりするために使います。
     * 絶対にフロントエンドに露出させてはいけません。
     *
     * 取得場所: Stripeダッシュボード → 開発者 → APIキー → シークレットキー
     */
    'secret' => env('STRIPE_SECRET', ''),

    /**
     * WebhookのシークレットキーStripe署名検証用）
     *
     * Stripeからの通知（Webhook）が本物かどうかを確認するために使います。
     * 偽のWebhookを受け取って誤動作するのを防ぎます。
     *
     * 取得場所: Stripeダッシュボード → 開発者 → Webhook → エンドポイント → 署名シークレット
     */
    'webhook_secret' => env('STRIPE_WEBHOOK_SECRET', ''),

    /**
     * 通貨設定
     *
     * 日本円（JPY）を指定します。
     * 日本円は「最小通貨単位」が1円のため、金額をそのまま整数で渡します。
     * （USDなどは1ドル = 100セントとして 100倍にして渡す必要があります）
     */
    'currency' => 'jpy',

    /**
     * 料金設定（CLAUDE.md準拠）
     *
     * ここに定義することで、金額を変更したい場合に
     * このファイルだけ修正すれば全体に反映されます。
     */
    'prices' => [
        // 正会員の月謝（円）
        'monthly_fee'  => 9000,
        // ビジターの都度払い（円）
        'visitor_fee'  => 2500,
    ],

];
